#include <stdio.h>  // เป็นไลบรารีมาตรฐานที่จำเป็นสำหรับการรับและแสดงผลข้อมูล

int main() {  // ประกาศตัวแปรชนิด float (จำนวนจริงมีทศนิยม) สำหรับเก็บคะแนน midterm และ final score
    float score;
    float finalScore;

    // 1: การรับค่าคะแนนจากผู้ใช้
    printf("Please enter your midterm score: ");  // โปรแกรมจะแสดงข้อความนี้เพื่อแจ้งให้ผู้ใช้ทราบว่าต้องป้อนข้อมูล
    
    scanf("%f", &score);  // คำสั่ง scanf() จะรอรับค่าตัวเลขทศนิยมจากผู้ใช้และเก็บไว้ในตัวแปร score

    // 2: การตรวจสอบเงื่อนไขและคำนวณคะแนน
    if (score >= 50.0) {  // ใช้คำสั่ง 'if' เพื่อตรวจสอบว่าคะแนนที่รับมามีค่าตั้งแต่ 50 ขึ้นไปหรือไม่ 
        // ถ้าเงื่อนไขเป็นจริง (คะแนน >= 50)
        finalScore = score + (score * 0.05);  // โปรแกรมจะคำนวณคะแนนรวมโดยเพิ่ม 5% ของคะแนน midterm เข้าไปในคะแนนเดิม
    } else {
        // ถ้าเงื่อนไขเป็นเท็จ (คะแนน < 50)
        // โปรแกรมจะใช้คะแนนเดิมเป็นคะแนนสุดท้าย
        finalScore = score;
    }

    // 3: การแสดงผลลัพธ์
    printf("Your total score is: %.2f\n", finalScore);  // แสดงคะแนนรวมสุดท้ายที่คำนวณได้ โดยแสดงทศนิยม 2 ตำแหน่ง

    // 4: การแสดงข้อความสิ้นสุดการประเมิน
    printf("End of evaluation\n");  // พิมพ์ข้อความ "End of evaluation" ตามที่โจทย์กำหนด

    return 0; // ส่งค่ากลับ 0 เพื่อบอกว่าการทำงานของโปรแกรมเสร็จสมบูรณ์
}