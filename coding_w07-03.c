#include <stdio.h> // เป็นไลบรารีมาตรฐานที่จำเป็นสำหรับการรับและแสดงผลข้อมูล

int main() {
    int level;
    
    // แจ้งให้ผู้ใช้ป้อนระดับ (level) และรับค่าจากแป้นพิมพ์
    printf("Enter level (1-4): ");
    scanf("%d", &level);
    
    // ตรวจสอบความถูกต้องของค่าที่ป้อนเข้ามาตามเงื่อนไขที่ผู้ใช้งานต้องการ (ระดับต้องอยู่ระหว่าง 1-4)
    if (level < 1 || level > 4) {
        printf("Invalid level\n");
    } else {
        // คำสั่ง switch ที่ได้รับการปรับปรุงแล้ว
        switch (level) {
            case 1:
                printf("Beginner\n");
                break; // เพิ่ม 'break' เพื่อป้องกันการทำงานต่อเนื่อง (fall-through)
            case 2:
                printf("Intermediate\n");
                break; // เพิ่ม 'break' เพื่อป้องกันการทำงานต่อเนื่อง
            case 3:
                printf("Advanced\n");
                break; // คำสั่งนี้มีอยู่แล้วในโค้ดต้นฉบับ
            case 4:
                printf("Expert\n");
                break; // คำสั่งนี้มีอยู่แล้วในโค้ดต้นฉบับ
        }
    }
    
    return 0;
}

// อภิปรายผลลัพธ์ 
// การวิเคราะห์ผลลัพธ์จากโค้ดต้นฉบับ:
// เมื่อผู้ใช้ป้อนค่า 1, โปรแกรมจะแสดง "Beginner" และตามด้วย "Intermediate" เนื่องจากไม่มี break ที่ case 1 ทำให้เกิด fall-through
// เมื่อผู้ใช้ป้อนค่า 2, โปรแกรมจะแสดง "Intermediate" และตามด้วย "Advanced" เนื่องจากไม่มี break ที่ case 2 ทำให้เกิด fall-through
// เมื่อผู้ใช้ป้อนค่า 3 และ 4, โปรแกรมทำงานได้ถูกต้อง
// เมื่อผู้ใช้ป้อนค่าอื่นๆ เช่น 0, -4, 10, 100, โปรแกรมจะแสดง "Invalid level" จากคำสั่ง default

// การอภิปรายผลลัพธ์หลังการปรับปรุงโค้ด:
// โค้ดที่ปรับปรุงแล้วมีการเพิ่มเงื่อนไข if-else เพื่อตรวจสอบค่าที่ผู้ใช้ป้อนก่อนเข้าสู่ switch ทำให้สามารถจัดการกับค่าที่ไม่ถูกต้องได้อย่างเป็นระบบ
// นอกจากนี้ยังได้เพิ่มคำสั่ง break ใน case 1 และ case 2 ซึ่งทำให้โปรแกรมทำงานได้ถูกต้องตามวัตถุประสงค์ นั่นคือจะแสดงข้อความที่เกี่ยวข้องกับ level ที่ป้อนเข้ามา
// เพียงอย่างเดียวและหยุดการทำงานทันที
// เช่น เมื่อป้อนค่า 1 จะแสดงเพียง "Beginner" เท่านั้น
// เมื่อป้อนค่า 2 จะแสดงเพียง "Intermediate" เท่านั้น